function scanRow(word, row, emoji) {
    var wholeRow = "";
    for (var l = 0; l < word.length; l++) {
        wholeRow += swichArray(word.charAt(l))[row].join('').replace(/0/g, emoji).replace(/\s/g, ":empty:");
    }
    return wholeRow;
}

